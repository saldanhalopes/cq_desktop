public class Setor {
    
    private int setor_id;
    private String setor;
    private String sigla_setor;
    private String descricao_setor;

    public int getSetor_id() {
        return setor_id;
    }

    public void setSetor_id(int setor_id) {
        this.setor_id = setor_id;
    }

    public String getSetor() {
        return setor;
    }

    public void setSetor(String setor) {
        this.setor = setor;
    }

    public String getSigla_setor() {
        return sigla_setor;
    }

    public void setSigla_setor(String sigla_setor) {
        this.sigla_setor = sigla_setor;
    }

    public String getDescricao_setor() {
        return descricao_setor;
    }

    public void setDescricao_setor(String descricao_setor) {
        this.descricao_setor = descricao_setor;
    }

    
}
