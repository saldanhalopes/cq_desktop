public class ColunaConfig {

    private int coluna_config_id;
    private String tipo_coluna_config;
    private String coluna_config;
    private String descricao_coluna_config;

    public int getColuna_config_id() {
        return coluna_config_id;
    }

    public void setColuna_config_id(int coluna_config_id) {
        this.coluna_config_id = coluna_config_id;
    }

    public String getTipo_coluna_config() {
        return tipo_coluna_config;
    }

    public void setTipo_coluna_config(String tipo_coluna_config) {
        this.tipo_coluna_config = tipo_coluna_config;
    }

    public String getColuna_config() {
        return coluna_config;
    }

    public void setColuna_config(String coluna_config) {
        this.coluna_config = coluna_config;
    }

    public String getDescricao_coluna_config() {
        return descricao_coluna_config;
    }

    public void setDescricao_coluna_config(String descricao_coluna_config) {
        this.descricao_coluna_config = descricao_coluna_config;
    }

    
}
