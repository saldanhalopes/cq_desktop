    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNomeCampanha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nome da Campanha inválido");
            txtNomeCampanha.requestFocus();
        } else if (txtCodMetodo1.getText().isEmpty() && txtCodMetodo2.getText().isEmpty()
                && txtCodMetodo3.getText().isEmpty() && txtCodMetodo4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione uma metodologia");
        } else {
            StringBuilder sb = new StringBuilder();
            sb.append("a").append(txtCodMetodo1.getText());
            sb.append("b").append(txtCodMetodo2.getText());
            sb.append("c").append(txtCodMetodo3.getText());
            sb.append("d").append(txtCodMetodo4.getText());

            FrmDialogSalvar frmSalvar = new FrmDialogSalvar(null, true, sys_name, "1",
                    txtNomeCampanha.getText().toUpperCase(), sb.toString(), txtObs.getText().toUpperCase());
            frmSalvar.setVisible(true);
            if (FrmDialogSalvar.senha()) {
                FrmFaseMovelCromatografo frmLogFM = new FrmFaseMovelCromatografo(null, true, sys_name);
                FrmFaseMovelCromatografo.btnCancelar.setVisible(false);
                FrmFaseMovelCromatografo.btnAguardando.setVisible(true);
                frmLogFM.setVisible(true);
                if (FrmDialogSalvar.senha()) {
                    FrmColunaCromatografo frmCol = new FrmColunaCromatografo(null, true, sys_name);
                    FrmColunaCromatografo.btnCancelar.setVisible(false);
                    FrmColunaCromatografo.btnAguardando.setVisible(true);
                    frmCol.setVisible(true);
                    if (FrmDialogSalvar.senha()) {
                        FrmSystemCromatografo frmSystem = new FrmSystemCromatografo(null, true, sys_name);
                        FrmSystemCromatografo.btnCancelar.setVisible(false);
                        FrmSystemCromatografo.btnAguardando.setVisible(true);
                        frmSystem.setVisible(true);
                    }
                }
                dispose();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

