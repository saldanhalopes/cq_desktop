    public final void readMetodologia() {
        DefaultTableModel model = (DefaultTableModel) tblMetodologia.getModel();
        MetodologiaDAO equipDao = new MetodologiaDAO();
        model.setNumRows(0);
        for (Metodologia mtd : equipDao.read()) {
            model.addRow(new Object[]{
                mtd.getMetodo_id(),
                mtd.getCod_metodo(),
                mtd.getMetodo(),
                mtd.getVersao()
            });
        }
    }

    private void carregarMetodo() {
        readMetodologia();
        pnlRegistros.setEnabledAt(0, false);
        pnlRegistros.setEnabledAt(1, true);
        pnlRegistros.setSelectedIndex(1);
    }

    private void carregarDados(String cod, String metodo) {
        switch (novoMetodologia) {
            case 1:
                txtCodMetodo1.setText(cod);
                txtMetodo1.setText(metodo);
                break;
            case 2:
                txtCodMetodo2.setText(cod);
                txtMetodo2.setText(metodo);
                break;
            case 3:
                txtCodMetodo3.setText(cod);
                txtMetodo3.setText(metodo);
                break;
            case 4:
                txtCodMetodo4.setText(cod);
                txtMetodo4.setText(metodo);
                break;
        }
    }

    private void fecharDados() {
        pnlRegistros.setEnabledAt(0, true);
        pnlRegistros.setEnabledAt(1, false);
        pnlRegistros.setSelectedIndex(0);
    }

